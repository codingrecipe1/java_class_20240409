package ch02_operator;

public class Ex02_ComparisonOperator {

	public static void main(String[] args) {
		System.out.println(10 > 5);
		System.out.println(10 < 5);
		boolean result = (8 <= 10);
		System.out.println(result);
		result = (8 == 10);
		System.out.println(result);
		result = (8 != 10);
		System.out.println(result);

	}

}
